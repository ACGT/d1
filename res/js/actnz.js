$(document).ready(function(){ 
var intervalID09; 
var curLi09; 
$(".n09body .n09list  li").mouseover(function(){ 
curLi09=$(this); 
intervalID09=setInterval(onMouseOver,250);//鼠标移入的时候有一定的延时才会切换到所在项，防止用户不经意的操作 
}); 
function onMouseOver(){ 
$(".n09body .n09top-con").removeClass("n09top-con"); 
$(".n09body .n09top").eq($(".n09body .n09list  li").index(curLi09)).addClass("n09top-con"); 
$(".n09body .cur").removeClass("cur"); 
curLi09.addClass("cur");
} 
$(".n09body .n09list  li").mouseout(function(){ 
clearInterval(intervalID09); 
});

});
